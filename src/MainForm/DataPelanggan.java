    private void tblpelanggan() {
        Object[] Baris = {"Kode Pelanggan", "Nama Pelanggan", "Alamat", "No.Telp"};
        tabmode = new DefaultTableModel(null, Baris);
        tblpelanggan.setModel(tabmode);
        String sql = "select * from tb_pelanggan";
        try {
            Statement stat = konek.koneksiDb().createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String kd_pelanggan = hasil.getString("kd_pelanggan");
                String nm_pelanggan = hasil.getString("nm_pelanggan");
                String alamat = hasil.getString("alamat");
                String no_telp = hasil.getString("no_telp");
                String[] data = {kd_pelanggan,nm_pelanggan,alamat,no_telp};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
        }
    }
     
