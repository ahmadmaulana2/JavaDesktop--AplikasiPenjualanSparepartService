    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        
        String tombol = tambah.getText();
        String kd_pelanggan = txkode.getText();
        String nm_pelanggan = txnama.getText();
        String alamat = txalamat.getText();
        String no_telp = txtelepon.getText();
               
        if (tombol.equals("TAMBAH")) {
            aktif();
            kosong();
            tambah.setText("SIMPAN");
            update.setEnabled(false);
            hapus.setEnabled(false);
            }else {
                if(txnama.getText().equals("NAMA PELANGGAN") || txalamat.getText().equals("ALAMAT")|| txtelepon.getText().equals("NO TELEPON")){
                    JOptionPane.showMessageDialog(this, "Harap Lengkapi Data ","Data Belum Lengkap",JOptionPane.WARNING_MESSAGE);
            }else{
            JOptionPane.showConfirmDialog(null, "Apakah Data anda sudah benar?", "INFORMASI",JOptionPane.YES_NO_OPTION);
                        try {
                            Connection c = konek.koneksiDb();
                            String sql = "INSERT INTO tb_pelanggan VALUES (?, ?, ?, ?)";
                            PreparedStatement p = c.prepareStatement(sql);
                            p.setString(1, kd_pelanggan);
                            p.setString(2, nm_pelanggan);
                            p.setString(3, alamat);
                            p.setString(4, no_telp);
                            p.executeUpdate();
                            p.close();
                            txkode.requestFocus();
                            } catch (SQLException e) {
                            System.out.println("Terjadi Error");
                            }finally{
                                String   msg="<html>Kode Pelanggan         = " +txkode.getText()+" <br>"
                                + "Nama Pelanggan        = " +txnama.getText()+"<br>"
                                + "Alamat              = " +txalamat.getText()+"<br>"
                                + "No.Telp             = " +txtelepon.getText()+"<html>";
                                JOptionPane optionPane=new JOptionPane();
                                optionPane.setMessage(msg);
                                optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
                                JDialog dialog=optionPane.createDialog(null, "DATA DISIMPAN");
                                dialog.setVisible(true);
                            }
                            new DataPelanggan().setVisible(true);
                            this.dispose();
                            autoNumber();
                            tblpelanggan();
                            tambah.setText("TAMBAH");
                            non_aktif();
            }
            }
    }//GEN-LAST:event_tambahActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if (update.getText()=="EDIT") {
            aktif();
            update.setText("UPDATE");
            hapus.setEnabled(false);
            tambah.setEnabled(false);
        } else {
            String sql = "update tb_pelanggan set nm_pelanggan=?, alamat=?, no_telp=? where kd_pelanggan='"+txkode.getText()+"'";
            try {
                PreparedStatement stat = konek.koneksiDb().prepareStatement(sql);

                stat.setString(1, txnama.getText());
                stat.setString(2, txalamat.getText());
                stat.setString(3, txtelepon.getText());
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Pelanggan berhasil Di Update");
                update.setText("EDIT");
                tblpelanggan();
                kosong();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data Pelanggan Gagal Di Update" + e);
            }
            new DataPelanggan().setVisible(true);
                            this.dispose();
        }
    }//GEN-LAST:event_updateActionPerformed

