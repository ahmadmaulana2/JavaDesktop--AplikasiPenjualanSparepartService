      private void autoNumber() {
        try {
            String sql = "SELECT * FROM tb_mekanik ORDER BY kd_mekanik DESC";
            PreparedStatement stat = konek.koneksiDb().prepareCall(sql);
            ResultSet rs = stat.executeQuery(sql);
            if (rs.next()) {
                String kd = rs.getString("kd_mekanik").substring(2);
                String AN =  "" + (Integer.parseInt(kd) + 1);
                String Nol = "";

                if (AN.length()==1) {
                    Nol = "000";
                } else if (AN.length()==2) {
                    Nol = "00";
                } else if (AN.length()==3) {
                    Nol = "0";
                } else if (AN.length()==4) {
                    Nol = "";
                }
                txkode.setText("MK" + Nol + AN);
            } else {
                txkode.setText("MK0001");
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

